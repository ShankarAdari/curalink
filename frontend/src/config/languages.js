export const getLanguage = (code) =>
  INDIAN_LANGUAGES.find(l => l.code === code) || INDIAN_LANGUAGES[0]

export const getSpeechLang = (code) => {
  // Map to browser-supported codes
  const map = {
    'mai-IN': 'hi-IN',  // Maithili → Hindi (browser fallback)
    'bho-IN': 'hi-IN',  // Bhojpuri → Hindi
    'raj-IN': 'hi-IN',  // Rajasthani → Hindi
    'doi-IN': 'hi-IN',  // Dogri → Hindi
    'kok-IN': 'hi-IN',  // Konkani → Hindi
    'mni-IN': 'bn-IN',  // Manipuri → Bengali
    'sd-IN':  'ur-IN',  // Sindhi → Urdu
  }
  return map[code] || code
}

// Language-specific system prompts
export const getLangPrompt = (langCode) => {
  if (langCode === 'en-US' || !langCode) return ''
  const lang = getLanguage(langCode)
  if (!lang) return ''
  return `\n\nIMPORTANT: The user speaks ${lang.name} (${lang.native}). Respond ENTIRELY in ${lang.name}. Use ${lang.native} script. Keep medical terms in English but explain everything else in ${lang.name}.`
}
